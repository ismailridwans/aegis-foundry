#!/usr/bin/env python3
"""Aegis Foundry custom alert action: ``aegis_triage``.

Splunk invokes this script with ``--execute`` and a JSON payload on stdin
(custom alert action protocol v2, ``payload_format = json``). The script:

1. Reads the payload: search name, sid, the first triggering result row, the
   splunkd ``server_uri`` and the ``session_key`` scoped to this action.
2. Composes a triage note for an analyst:
   - If an OpenAI-compatible endpoint is configured via environment
     variables (``AEGIS_TRIAGE_AI_URL`` et al. - e.g. Foundation-Sec-1.1-8B
     served by Ollama/vLLM, the same place the AI Toolkit ``| ai`` command
     would point), the note is generated by the model.
   - Otherwise - or on ANY model failure - it falls back to a deterministic
     severity heuristic, so the action never blocks on an external service
     and behaves identically in offline/mock demos.
3. Writes the note back into Splunk through the management REST
   ``receivers/simple`` endpoint (index ``aegis_audit``, sourcetype
   ``aegis:triage_note``) using the session key from the payload, closing
   the loop: the triage of an Aegis-forged detection lands in the same
   audited index the agents write to.

Standard library only (``urllib``); runs on the Splunk-bundled Python 3.

Environment variables (all optional):
    AEGIS_TRIAGE_AI_URL      OpenAI-compatible chat-completions endpoint,
                             e.g. http://localhost:11434/v1/chat/completions
    AEGIS_TRIAGE_AI_KEY      Bearer token for that endpoint (omit for Ollama).
    AEGIS_TRIAGE_AI_MODEL    Model name (default foundation-sec-1.1-8b-instruct).
    AEGIS_TRIAGE_AI_TIMEOUT  Seconds to wait for the model (default 20).
    AEGIS_AUDIT_INDEX        Destination index (default aegis_audit).
    AEGIS_SPLUNK_VERIFY_TLS  "true" to verify splunkd's TLS cert when posting
                             the note (default false: the local management
                             port typically presents a self-signed cert).
"""

from __future__ import annotations

import json
import os
import ssl
import sys
import urllib.error
import urllib.parse
import urllib.request
from datetime import datetime, timezone
from typing import Any, Optional

SOURCETYPE = "aegis:triage_note"
SOURCE = "aegis_triage"
DEFAULT_MODEL = "foundation-sec-1.1-8b-instruct"

SEVERITY_LABELS = {1: "low", 2: "medium", 3: "high", 4: "critical"}


def log(level: str, message: str) -> None:
    """Write a log line to stderr; splunkd captures it into splunkd.log."""
    sys.stderr.write(f"{level} {SOURCE}: {message}\n")
    sys.stderr.flush()


def utc_now_iso() -> str:
    """ISO-8601 UTC timestamp with microseconds, matching the props.conf
    TIME_FORMAT (%Y-%m-%dT%H:%M:%S.%6N%:z) for sourcetype aegis:triage_note."""
    return datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%S.%f") + "+00:00"


def _ssl_context(verify: bool) -> ssl.SSLContext:
    """Build an SSL context; unverified only when explicitly allowed (the
    splunkd management port usually presents a self-signed certificate)."""
    if verify:
        return ssl.create_default_context()
    ctx = ssl.create_default_context()
    ctx.check_hostname = False
    ctx.verify_mode = ssl.CERT_NONE
    return ctx


def assess_severity(result: dict[str, Any]) -> tuple[str, list[str]]:
    """Deterministic severity heuristic over the triggering result row.

    Scores escalate on PowerShell execution, encoded command lines, and
    credential-access tooling indicators; service-account context is flagged
    as a possible allowlisting candidate rather than escalated.
    """
    cmd = str(result.get("CommandLine", "")).lower()
    proc = str(result.get("process_name", "")).lower()
    user = str(result.get("user", "")).lower()
    score = 1
    reasons: list[str] = []

    if "powershell" in proc or "powershell" in cmd:
        score = max(score, 2)
        reasons.append("PowerShell execution observed (T1059.001 pattern)")
    if "-encodedcommand" in cmd or " -enc " in f" {cmd}":
        score = max(score, 3)
        reasons.append("encoded command line: obfuscation consistent with credential-theft tradecraft")
    if any(token in cmd for token in ("mimikatz", "lsass", "sekurlsa", "comsvcs")):
        score = max(score, 4)
        reasons.append("credential-access tooling indicator on the command line (T1003.001 pattern)")
    if user.startswith("svc_"):
        reasons.append(f"service-account context ({user}): verify against deployment allowlists before escalating")
    if not reasons:
        reasons.append("no high-signal indicators in the triggering event; review for context")

    return SEVERITY_LABELS[score], reasons


def heuristic_note(search_name: str, severity: str, reasons: list[str], result: dict[str, Any]) -> str:
    """Compose the deterministic fallback triage note."""
    host = result.get("host", "unknown-host")
    user = result.get("user", "unknown-user")
    lines = [
        f"Triage note for alert '{search_name}' (severity assessment: {severity}).",
        f"Subject event: host={host} user={user}.",
        "Assessment factors:",
    ]
    lines.extend(f"  - {reason}" for reason in reasons)
    lines.append(
        "Recommended next step: pivot on the host/user pair over the last 24h and "
        "check the Aegis flight recorder for the rule's backtest precision and "
        "noise forecast before paging on-call."
    )
    return "\n".join(lines)


def ai_note(search_name: str, severity: str, reasons: list[str], result: dict[str, Any]) -> Optional[str]:
    """Ask an env-configured OpenAI-compatible endpoint for a triage note.

    This is the plug-in point for Foundation-Sec / ``| ai``-equivalent
    reasoning. Returns None when unconfigured or on any failure, so callers
    always have the deterministic heuristic to fall back on.
    """
    url = os.environ.get("AEGIS_TRIAGE_AI_URL", "").strip()
    if not url:
        return None
    model = os.environ.get("AEGIS_TRIAGE_AI_MODEL", DEFAULT_MODEL).strip() or DEFAULT_MODEL
    api_key = os.environ.get("AEGIS_TRIAGE_AI_KEY", "").strip()
    try:
        timeout = float(os.environ.get("AEGIS_TRIAGE_AI_TIMEOUT", "20"))
    except ValueError:
        timeout = 20.0

    sample = {
        k: result.get(k)
        for k in ("host", "user", "process_name", "CommandLine", "EventCode")
        if k in result
    }
    prompt = (
        f"Splunk alert '{search_name}' fired. Heuristic severity: {severity}. "
        f"Heuristic factors: {'; '.join(reasons)}. "
        f"Triggering event fields: {json.dumps(sample, default=str)}. "
        "Write a concise SOC triage note (max 120 words): likely technique, "
        "whether this looks like a true positive, and the single best next "
        "investigative step."
    )
    body = {
        "model": model,
        "messages": [
            {
                "role": "system",
                "content": "You are a senior SOC analyst writing terse, actionable triage notes.",
            },
            {"role": "user", "content": prompt},
        ],
        "temperature": 0.1,
        "max_tokens": 300,
    }
    headers = {"Content-Type": "application/json"}
    if api_key:
        headers["Authorization"] = f"Bearer {api_key}"

    request = urllib.request.Request(
        url, data=json.dumps(body).encode("utf-8"), headers=headers, method="POST"
    )
    context = _ssl_context(verify=True) if url.lower().startswith("https") else None
    try:
        with urllib.request.urlopen(request, timeout=timeout, context=context) as response:
            data = json.loads(response.read().decode("utf-8"))
        text = str(data["choices"][0]["message"]["content"]).strip()
        return text or None
    except Exception as exc:  # any failure -> deterministic fallback
        log("WARN", f"AI endpoint unavailable, using heuristic note ({exc})")
        return None


def post_note(server_uri: str, session_key: str, index: str, event: dict[str, Any]) -> None:
    """Index the triage note via the REST receivers/simple endpoint."""
    query = urllib.parse.urlencode(
        {"index": index, "sourcetype": SOURCETYPE, "source": SOURCE}
    )
    url = f"{server_uri.rstrip('/')}/services/receivers/simple?{query}"
    request = urllib.request.Request(
        url,
        data=json.dumps(event, default=str).encode("utf-8"),
        headers={"Authorization": f"Splunk {session_key}"},
        method="POST",
    )
    verify = os.environ.get("AEGIS_SPLUNK_VERIFY_TLS", "false").strip().lower() in (
        "1",
        "true",
        "yes",
    )
    context = _ssl_context(verify) if url.lower().startswith("https") else None
    with urllib.request.urlopen(request, timeout=30, context=context) as response:
        response.read()


def execute(payload: dict[str, Any]) -> int:
    """Run the alert action against a parsed stdin payload. Returns exit code."""
    search_name = str(payload.get("search_name", "unknown-alert"))
    sid = str(payload.get("sid", ""))
    result = payload.get("result") or {}
    server_uri = str(payload.get("server_uri", "")).strip()
    session_key = str(payload.get("session_key", "")).strip()
    if not server_uri or not session_key:
        log("ERROR", "payload missing server_uri/session_key; cannot write triage note")
        return 2

    severity, reasons = assess_severity(result)
    note_text = ai_note(search_name, severity, reasons, result)
    note_source = "ai" if note_text else "heuristic"
    if note_text is None:
        note_text = heuristic_note(search_name, severity, reasons, result)

    event = {
        "ts": utc_now_iso(),
        "agent": "triage-analyst",
        "action": "triage_note",
        "search_name": search_name,
        "sid": sid,
        "severity": severity,
        "note_source": note_source,
        "note": note_text,
        "result_sample": {
            k: result.get(k)
            for k in ("host", "user", "process_name", "CommandLine", "EventCode")
            if k in result
        },
    }
    index = os.environ.get("AEGIS_AUDIT_INDEX", "aegis_audit").strip() or "aegis_audit"
    try:
        post_note(server_uri, session_key, index, event)
    except (urllib.error.URLError, OSError) as exc:
        log("ERROR", f"failed to index triage note via receivers/simple: {exc}")
        return 3
    log("INFO", f"triage note ({note_source}, severity={severity}) indexed for alert '{search_name}'")
    return 0


def main(argv: list[str]) -> int:
    """Entry point per the Splunk custom alert action protocol."""
    if "--execute" not in argv:
        log("FATAL", "unsupported invocation; expected --execute with JSON payload on stdin")
        return 1
    try:
        payload = json.loads(sys.stdin.read())
    except json.JSONDecodeError as exc:
        log("FATAL", f"invalid JSON payload on stdin: {exc}")
        return 1
    return execute(payload)


if __name__ == "__main__":
    sys.exit(main(sys.argv[1:]))
